package java_basics;

public class JavaOperators {
    static void main(String[] args) {
        int number_one = 4;
        int number_two = 2;

        int result_addition = number_one + number_two;
        System.out.println(result_addition);

        int result_subtraction = number_one - number_two;
        System.out.println(result_subtraction);

        float result_division = number_one / number_two;
        System.out.println(result_division);

        int result_multiplication = number_one * number_two;
        System.out.println(result_multiplication);
    }
}
